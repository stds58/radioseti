def convert_to_decimal(metrics:List[tuple]) -> dict:
    data_new = {}
    for item_tuple in metrics:
        degree, density = item_tuple
        if degree == 360:
            degree = 0
        data_new[degree] = Decimal(f"{density}")
    return data_new

